#include "libft.h"

char	*ft_strdup(const char *s1)

{
	int		i;
	char	*dup;

	i = 0;
	dup = ft_calloc(sizeof(char), (ft_strlen(s1) + 1));
	if (!dup)
		return (NULL);
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	return (dup);
}
